// Derive journal status from whether a journal entry exists for a trade
export function getJournalStatus(
  tradeId: string,
): "journaled" | "missing" | "none" {
  const journal = journals.find((j) => j.tradeId === tradeId);
  if (journal) return "journaled";
  // Some trades are marked as missing (no journal but should have one)
  const missingIds = ["TRD-8919", "TRD-8916", "TRD-8910"];
  if (missingIds.includes(tradeId)) return "missing";
  return "none";
}

